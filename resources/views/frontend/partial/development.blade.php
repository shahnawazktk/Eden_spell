<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Count Up Animation
        const countUpElements = document.querySelectorAll('.count-up');
        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const element = entry.target;
                    const target = parseInt(element.getAttribute('data-target'));
                    const duration = 2000; // 2 seconds
                    const step = target / (duration / 16); // 60fps
                    let current = 0;
                    
                    const timer = setInterval(() => {
                        current += step;
                        if (current >= target) {
                            clearInterval(timer);
                            element.textContent = target;
                        } else {
                            element.textContent = Math.floor(current);
                        }
                    }, 16);
                    
                    observer.unobserve(element);
                }
            });
        }, { threshold: 0.5 });
        
        countUpElements.forEach(element => {
            observer.observe(element);
        });
        
        // Hover animations for process steps
        const processSteps = document.querySelectorAll('.group');
        
        processSteps.forEach(step => {
            step.addEventListener('mouseenter', function() {
                const visualizations = this.querySelectorAll('.h-full.bg-gradient-to-r');
                visualizations.forEach((viz, index) => {
                    setTimeout(() => {
                        viz.style.width = viz.style.width || '0';
                    }, index * 200);
                });
            });
        });
        
        // Animated typing effect for terminal
        const terminalSim = document.querySelector('.font-mono');
        if (terminalSim) {
            const lines = terminalSim.querySelectorAll('div');
            lines.forEach((line, index) => {
                line.style.opacity = '0';
                line.style.animation = `typing 0.5s ease-out ${index * 0.3}s forwards`;
            });
        }
        
        // Interactive process tags
        const processTags = document.querySelectorAll('.process-tag');
        processTags.forEach(tag => {
            tag.addEventListener('mouseenter', function() {
                this.style.transform = 'translateY(-2px) scale(1.05)';
                this.style.boxShadow = '0 4px 12px rgba(239, 68, 68, 0.2)';
            });
            
            tag.addEventListener('mouseleave', function() {
                this.style.transform = 'translateY(0) scale(1)';
                this.style.boxShadow = 'none';
            });
        });
        
        // Scroll animations
        const animatedElements = document.querySelectorAll('.animate-slide-up');
        const scrollObserver = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const delay = entry.target.style.animationDelay || '0s';
                    entry.target.style.animationDelay = delay;
                    entry.target.style.opacity = '1';
                }
            });
        }, { threshold: 0.1 });
        
        animatedElements.forEach(element => {
            scrollObserver.observe(element);
        });
    });
</script>