<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Tab Switching Functionality
        const tabs = document.querySelectorAll('.service-tab');
        const panes = document.querySelectorAll('.service-pane');
        
        tabs.forEach(tab => {
            tab.addEventListener('click', function() {
                // Remove active class from all tabs
                tabs.forEach(t => t.classList.remove('active'));
                tabs.forEach(t => {
                    t.className = t.className.replace('from-red-700 to-red-900', 'from-gray-900 to-black');
                    t.classList.add('border', 'border-gray-800', 'text-gray-400');
                });
                
                // Add active class to clicked tab
                this.classList.add('active');
                this.classList.remove('border-gray-800', 'text-gray-400');
                this.classList.add('from-red-700', 'to-red-900', 'text-white');
                
                // Hide all panes
                panes.forEach(pane => {
                    pane.classList.remove('active');
                    setTimeout(() => {
                        pane.style.display = 'none';
                    }, 300);
                });
                
                // Show selected pane
                const targetPane = document.getElementById(this.dataset.tab);
                setTimeout(() => {
                    targetPane.style.display = 'block';
                    setTimeout(() => {
                        targetPane.classList.add('active');
                    }, 10);
                }, 300);
            });
        });
        
        // Animate progress bars on scroll
        const observerOptions = {
            threshold: 0.5,
            rootMargin: '0px 0px -50px 0px'
        };
        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const progressBars = entry.target.querySelectorAll('.group:hover .transition-all');
                    progressBars.forEach(bar => {
                        bar.style.width = bar.style.width || '0';
                    });
                }
            });
        }, observerOptions);
        
        // Observe all service cards
        document.querySelectorAll('.group.relative').forEach(card => {
            observer.observe(card);
        });
        
        // Hover effect for tech tags
        const techTags = document.querySelectorAll('.tech-tag');
        techTags.forEach(tag => {
            tag.addEventListener('mouseenter', function() {
                this.style.transform = 'scale(1.05) translateY(-2px)';
            });
            
            tag.addEventListener('mouseleave', function() {
                this.style.transform = 'scale(1) translateY(0)';
            });
        });
    });
</script>